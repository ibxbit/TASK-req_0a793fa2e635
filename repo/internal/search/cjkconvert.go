var tc2sc map[rune]rune

func init() {
	tc2sc = make(map[rune]rune, len(sc2tc))
	for sc, tc := range sc2tc {
		tc2sc[tc] = sc
	}
}

// CJKVariants returns the input rune plus any script variant (SC↔TC).
// For non-CJK runes, returns a slice with just the original.
func CJKVariants(r rune) []rune {
	out := []rune{r}
	if tc, ok := sc2tc[r]; ok && tc != r {
		out = append(out, tc)
	}
	if sc, ok := tc2sc[r]; ok && sc != r {
		out = append(out, sc)
	}
	return out
}

// ExpandCJKToken returns variants of a (single-character) CJK token.
// Non-CJK tokens pass through unchanged.
func ExpandCJKToken(tok string) []string {
	if tok == "" {
		return nil
	}
	runes := []rune(tok)
	if len(runes) != 1 || !isCJK(runes[0]) {
		return []string{tok}
	}
	vs := CJKVariants(runes[0])
	out := make([]string, 0, len(vs))
	seen := map[string]struct{}{}
	for _, r := range vs {
		s := string(r)
		if _, ok := seen[s]; ok {
			continue
		}
		seen[s] = struct{}{}
		out = append(out, s)
	}
	return out
}
